package smart.bean;

public class Attention {
	private int at_id;
	private String at_moId;
	private String at_moIp;
	private String at_moName;
	private int  at_degree;
	private long at_collectTime;
	
	
	public int getAt_id() {
		return at_id;
	}
	public void setAt_id(int at_id) {
		this.at_id = at_id;
	}
	public String getAt_moId() {
		return at_moId;
	}
	public void setAt_moId(String at_moId) {
		this.at_moId = at_moId;
	}
	public String getAt_moIp() {
		return at_moIp;
	}
	public void setAt_moIp(String at_moIp) {
		this.at_moIp = at_moIp;
	}
	public String getAt_moName() {
		return at_moName;
	}
	public void setAt_moName(String at_moName) {
		this.at_moName = at_moName;
	}
	public int getAt_degree() {
		return at_degree;
	}
	public void setAt_degree(int at_degree) {
		this.at_degree = at_degree;
	}
	public long getAt_collectTime() {
		return at_collectTime;
	}
	public void setAt_collectTime(long at_collectTime) {
		this.at_collectTime = at_collectTime;
	}
	
	
}
