package smart.dto;

public class Progress {

	private String pro_id;
	private String pro_name;
	private int pro_total;
	private String pro_username;
	
	public Progress() {
	}

	public String getPro_id() {
		return pro_id;
	}

	public void setPro_id(String pro_id) {
		this.pro_id = pro_id;
	}

	public String getPro_name() {
		return pro_name;
	}

	public void setPro_name(String pro_name) {
		this.pro_name = pro_name;
	}

	public int getPro_total() {
		return pro_total;
	}

	public void setPro_total(int pro_total) {
		this.pro_total = pro_total;
	}

	public String getPro_username() {
		return pro_username;
	}

	public void setPro_username(String pro_username) {
		this.pro_username = pro_username;
	}

}
