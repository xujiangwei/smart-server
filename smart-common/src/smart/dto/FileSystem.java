package smart.dto;

public class FileSystem {

	private String fs_id;
	private String fs_name;
	private float fs_size;
	private String fs_type;
	
	public FileSystem() {
	}

	public String getFs_id() {
		return fs_id;
	}

	public void setFs_id(String fs_id) {
		this.fs_id = fs_id;
	}

	public String getFs_name() {
		return fs_name;
	}

	public void setFs_name(String fs_name) {
		this.fs_name = fs_name;
	}

	public float getFs_size() {
		return fs_size;
	}

	public void setFs_size(float fs_size) {
		this.fs_size = fs_size;
	}

	public String getFs_type() {
		return fs_type;
	}

	public void setFs_type(String fs_type) {
		this.fs_type = fs_type;
	}

}
