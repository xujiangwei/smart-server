package smart.dto;

public class NetInterface {

	private String ni_id;
	private String ni_name;
	private String ni_type;
	private String ni_desip;
	private String ni_ip;
	private String ni_mac;
	
	public NetInterface() {
	}

	public String getNi_id() {
		return ni_id;
	}

	public void setNi_id(String ni_id) {
		this.ni_id = ni_id;
	}

	public String getNi_name() {
		return ni_name;
	}

	public void setNi_name(String ni_name) {
		this.ni_name = ni_name;
	}

	public String getNi_type() {
		return ni_type;
	}

	public void setNi_type(String ni_type) {
		this.ni_type = ni_type;
	}

	public String getNi_desip() {
		return ni_desip;
	}

	public void setNi_desip(String ni_desip) {
		this.ni_desip = ni_desip;
	}

	public String getNi_ip() {
		return ni_ip;
	}

	public void setNi_ip(String ni_ip) {
		this.ni_ip = ni_ip;
	}

	public String getNi_mac() {
		return ni_mac;
	}

	public void setNi_mac(String ni_mac) {
		this.ni_mac = ni_mac;
	}

}
